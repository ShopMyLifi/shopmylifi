	public boolean isPackageExisted(String targetPackage) {
		List<ApplicationInfo> packages;
		PackageManager pm;
		pm = getPackageManager();
		packages = pm.getInstalledApplications(0);
		for (ApplicationInfo packageInfo : packages) {
			if (packageInfo.packageName.equals(targetPackage))
				return true;
		}
		return false;
	}
	
	public void matriceToText(int[][] matrice) {

		try {
			File sdCard = Environment.getExternalStorageDirectory();
			File dir = new File (sdCard.getAbsolutePath());
			dir.mkdirs();
		    File file = new File(dir, "matrice.txt");
		    FileOutputStream f = new FileOutputStream(file);
	        PrintWriter pw = new PrintWriter(f);
			int dim1 = matrice.length;
			int dim2 = matrice[0].length; //endur

			for (int b = dim2-1; b >= 0 ; b--) {

				for (int a = 0; a < dim1 ; a++) {
					
			        pw.print(Integer.toString(matrice[a][b]));
			       
				}
				pw.println("");
			}
			pw.flush();
			pw.close();
			f.close();
			Log.d("debug string","tableau ecrit");
		} catch (IOException ex) {
			ex.printStackTrace();
		}

	}
